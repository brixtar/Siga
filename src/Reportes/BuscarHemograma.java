    private void cargarTabla(){
        String[] columNames = {"Protocolo Lab","Caso Clinico","DNI del Duenio","Apellido","Nombre","Nombre Del Animal"};
        String colName[] = {"protocolo_lab","caso_clinico","dni_duenio","apellido","nombre","animal_pequenios.nombre"};
        //nombre de columnas, tabla, instruccion sql
        dtCasoClinico = casoClinico.GetTabla(colName, "consulta,duenio,animal_pequenios,doctor,hemograma", "SELECT * FROM `consulta`,`duenio`,`animal_pequenios`,`doctor`,`hemograma` WHERE (`consulta`.`cod_animal` =`animal_pequenios`.`cod_animal`) AND (`consulta`.`dni_doctor` =`doctor`.`dni_doctor`) AND (`animal_pequenios`.`dni_duenio` =`duenio`.`dni_duenio`)AND (`consulta`.`caso_clinico` = `hemograma`.`caso_clinico`)");
        // se colocan los datos en la tabla
        DefaultTableModel datos = new DefaultTableModel(dtCasoClinico, columNames);
        jtCasoClinico.setModel(datos);
        // se colocan los datos en la tabla
        int a = jtCasoClinico.getRowCount();
        //depurador de jtDuenio de item null
        int i=0;
        a--;
        while(a>=i){
            if(jtCasoClinico.getValueAt(i,1)==null){
                ((DefaultTableModel)jtCasoClinico.getModel()).removeRow(i);
                a--;
                i--;
            }
            i++;
        }
    }
